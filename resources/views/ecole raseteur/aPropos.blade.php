            veut défendre son territoire.
            Au moment du "raset", lorsque le taureau et le raseteur se croisent, à
            l'aide de son crochet, le raseteur par un geste technique rapide essaie
            d'enlever un attribut. La cocarde d'abord, puis les deux glands et enfin
            la 1ère et 2eme ficelle enroulées autour de chaque corne du taureau.
            Le raseteur doit alors vite se dégager du taureau pour chercher refuge
            en sautant la barrière. Le bon cocardier poursuivra le raseteur
